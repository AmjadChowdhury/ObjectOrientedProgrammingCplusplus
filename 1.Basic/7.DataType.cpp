#include<iostream>
using namespace std;

int main()
{
    int num1 = 15;

    float num2 = 3.4;
    double num3 = 22.254;

    char ch = 'A';
    string greeting = "Hello!Welcome to my hotel california.";

    bool isCodingFun = true;

    cout<<"num1(int) : "<<num1<<endl;
    cout<<"num2(float) : "<<num2<<endl;
    cout<<"num3(double) : "<<num3<<endl;
    cout<<"ch(char) : "<<ch<<endl;
    cout<<"greeting(string) : "<<greeting<<endl;
    cout<<"isCodingFun(bool) : "<<isCodingFun<<endl;

    return 0;
}
