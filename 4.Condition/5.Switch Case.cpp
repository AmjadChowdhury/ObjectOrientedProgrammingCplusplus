#include<iostream>
using namespace std;

int main()
{
    int num = 3;
    switch(num)
    {
    case 1:
        cout<<"This is 1."<<endl;
        cout<<"One."<<endl;
        break;
    case 2:
        cout<<"This is 2."<<endl;
        cout<<"Two."<<endl;
        break;
    case 3:
        cout<<"This is 3."<<endl;
        cout<<"Three."<<endl;
        break;
    default:
        cout<<"Nothing else"<<endl;
    }



    return 0;
}
